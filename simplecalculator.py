print("Simple Calculator") #Use of print statement
x = 10 #Integer data type can be changed
y = 20 #Integer data type can be changed

sum = x + y #Sum of x and y
difference = x - y #Difference of x and y
product = x * y #Product of x and y
quotient = x / y #Quotient of x by y
remainder = x % y #Remainder of x by y

print("The sum of x and y is ", sum) #Printing the sum
print("The difference between x and y is ", difference) #Printing the difference
print("The product of x and y is ",product)
print("The quotient of x by y is ", quotient) #Printing the Quotient
print("The remainder of x by y is ", remainder) #Printing the Remainder
